var i;

// For loop
for (i = 0; i < 4; i++) {
        if (i == 0) {
                console.log("Here I = 0")
        } else if (i < 2) {
                console.log("Here I = 1")
        } else {
                console.log("Here I > 2")
        }
}

// While loop
while (i < 10) {
        console.log("The number is " + i);
        i++;
}
